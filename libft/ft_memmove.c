#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;
	size_t			i;

	if (!dest && !src)
		return (NULL);
	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	if (d < s)
	{
		i = 0;
		while (i < n)
		{
			d[i] = s[i];
			i++;
		}
	}
	else
	{
		while (n-- > 0)
			d[n] = s[n];
	}
	return (dest);
}
// DESCRIPTION
// The  memmove()  function  copies  n bytes from memory area src to memory
// area dest.  The memory areas may overlap: copying takes place as though
//the  bytes in  src  are  first copied  into a temporary array that does 
//not overlap src or dest, and the bytes are then copied from the temporary
//array to dest. The memmove() function returns a pointer to dest.