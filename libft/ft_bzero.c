#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*str;
	size_t			i;

	i = 0;
	str = (unsigned char *)s;
	while (i < n)
	{
		str[i] = '\0';
		i++;
	}
}
// The  bzero() function erases the data in the n bytes of the memory
// starting at the location pointed to by s, by writing zeros 
// (bytes containing '\0') to that area.
// return value - none