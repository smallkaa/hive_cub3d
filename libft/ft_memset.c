#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ptr;

	i = 0;
	ptr = (unsigned char *)s;
	while (i < n)
	{
		ptr[i] = (unsigned char)c;
		i++;
	}
	return (s);
}
// The memset() function writes len bytes 
// of value c (converted to an unsigned char) to the string b.
// RETURN VALUES
// The memset() function returns its first argument.
